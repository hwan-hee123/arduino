// gait_patterns.h
// 보행 패턴 정의 (서보 각도 배열)
//
// 각도 배열 순서: { LH, LK, LA, RH, RK, RA }
//   LH = Left  Hip   (왼쪽 허리)  90=중립, 70=앞, 110=뒤
//   LK = Left  Knee  (왼쪽 무릎) 90=펼침, 60=구부림
//   LA = Left  Ankle (왼쪽 발목) 90=중립
//   RH = Right Hip   (오른쪽 허리) 90=중립, 110=앞, 70=뒤  ← 마운팅 방향 반대
//   RK = Right Knee  (오른쪽 무릎) 90=펼침, 60=구부림
//   RA = Right Ankle (오른쪽 발목) 90=중립

#pragma once

// ── 기본 자세 (차렷) ─────────────────────────────────────────────────────────
const int NEUTRAL[6] = { 90, 90, 90, 90, 90, 90 };

// ── 전진 (7 프레임) ──────────────────────────────────────────────────────────
// 흐름: 기본 → 오른쪽 무게이동 → 왼발 들어 앞으로 → 왼발 착지
//      → 왼쪽 무게이동 → 오른발 들어 앞으로 → 오른발 착지
#define GAIT_FWD_FRAMES 7
const int GAIT_FORWARD[GAIT_FWD_FRAMES][6] = {
  { 90,  90,  90,  90,  90,  90 },  // 0: 기본 자세
  { 90,  90,  95,  90,  80,  85 },  // 1: 오른쪽으로 무게 이동
  { 70,  60, 110,  90,  80,  85 },  // 2: 왼발 들어 앞으로 스윙
  { 70,  90,  90,  90,  90,  90 },  // 3: 왼발 착지 (앞쪽)
  { 70,  80,  90,  90,  90,  95 },  // 4: 왼쪽으로 무게 이동
  { 70,  80,  90, 110,  60,  70 },  // 5: 오른발 들어 앞으로 스윙
  { 90,  90,  90, 110,  90,  90 },  // 6: 오른발 착지 → 다시 0으로
};

// ── 후진 (7 프레임) ──────────────────────────────────────────────────────────
